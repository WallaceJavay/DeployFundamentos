// Golang classes

package main

// Função para somar
func FuncSomar(a int, b int) int {
	return a + b
}

// Função para subtrair
func FuncSubtrair(a int, b int) int {
	return a - b
}

// Função para multiplicar
func FuncMultiplicar(a int, b int) int {
	return a * b
}

// Função para dividir
func FuncDividir(a int, b int) int {
	return a / b
}
